//Object -> It is a collection of properties

let person ={
    firstName:'Rajat',
    lastName:'Verma',
    country:'India'
}

console.log("1st way")
console.log("person.firstName: "+person.firstName)
console.log("person.lastName: "+person.lastName)
console.log("person.country: "+person.country)

console.log("2nd way")
console.log("person[firstName]: "+person['firstName'])
console.log("person[lastName]: "+person['lastName'])
console.log("person[country]: "+person['country'])
