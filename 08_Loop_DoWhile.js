let num1=1

//In Do-While Loop, 1 round will execute for sure
do{
    console.log(num1)

    //Post-increment
    num1++
}while(num1<11)

console.log("------------------")

num1=1
//In Do-While Loop, 1 round will execute for sure
do{
    console.log(num1)

    //Post-increment
    num1++
}while(num1<1) //Even if the condition is false, Code inside the do section executed for one time.

console.log("Success")